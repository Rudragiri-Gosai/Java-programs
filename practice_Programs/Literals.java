package practice_Programs;
public class Literals {
    public static void main(String[] args) {
        byte age = 34;
        int age2 = 54;
        short age3 = 87;
        long age4 = 566666666666666L;
        float f1 = 5.6f;
        double d1 = 5.6;
        char ch = 'A';
        boolean a = true;
        System.out.println("Use of Byte data type      : " + age);
        System.out.println("Use of int data type       : " + age2);
        System.out.println("Use of short data type     : " + age3);
        System.out.println("Use of long data type      : " + age4);
        System.out.println("Use of character data type : " + ch);
        System.out.println("Use of float data type     : " + f1);
        System.out.println("Use of Double data type    : " + d1);
        System.out.println("Use of boolean data type   : " + a);
    }
}
