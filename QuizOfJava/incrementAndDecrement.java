package QuizOfJava;

public class incrementAndDecrement {
    public static void main(String[] args) {
        int a = 50;
        System.out.println("A values :: ");
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        int b=50;
        System.out.println("B values :: ");
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);

    }
}
